    //If the "Store my information" tick box is ticked, the user can save 
    private void ibtcheckBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ibtcheckBTNActionPerformed
        // TODO add your handling code here:
       //code to store the message in a text file if the user ticks the box
        File outFile;
        FileWriter fw;
        BufferedWriter bw;
        
        //I want to store the message displayed for further search
        String message = ibddisplayTA.getText();
        
        try{
            outFile = new File("idealBedTime.txt");
            fw = new FileWriter(outFile, true); //true appends new data to the end, false will add to the beggining.
            bw = new BufferedWriter(fw);
            
           bw.write(message);
           bw.newLine();
           bw.close(); 
           
           JOptionPane.showMessageDialog(null, "Information was stored successfully." );
        }catch(IOException e){
            System.out.println("exception e: " + e.getMessage());           
        }
        
    }//GEN-LAST:event_ibtcheckBTNActionPerformed

