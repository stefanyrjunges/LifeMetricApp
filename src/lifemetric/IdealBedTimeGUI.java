    private void ibtcalculateBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ibtcalculateBTNActionPerformed
        // TODO add your handling code here:
        //code to call the WakeUpAt method (compute) and display the message
        
        int age = Integer.parseInt(ibtageTF.getText());
        IdealBedTime tempIBT = new IdealBedTime(age);
        
        wakeUpTime = Integer.parseInt(ibtwakeuptimeTF.getText());
        tempIBT.setWakeUpTime(wakeUpTime);
        
        String name = ibtnameTF.getText();
        tempIBT.setName(name);
        
        tempIBT.wakeUpAt();
        
        ibddisplayTA.setText(tempIBT.getMessage());
        
        
    }//GEN-LAST:event_ibtcalculateBTNActionPerformed

